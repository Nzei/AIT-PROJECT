    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        customer.setFirstName(FirstNameTextField.getText());
        customer.setLastName(LastNameTextField.getText());
        customer.setAddress(AddressTextField.getText());
        customer.setEmail(EmailTextField.getText());
        customer.setPhoneNumber(PhoneNumberTextField.getText());
        customer.setGender(GenderTextField.getText());
        customer.setDob(DOBTextField.getText());
        customer.setMothersMaidenName(MotherMaidenNameTextField.getText());
        customer.setAccountNumber(AccountNumberTextField.getText());
        Customer savedCustomer = customerService.saveCustomer(customer);
        JOptionPane.showMessageDialog(jScrollPane2, "Please save your customer ID: "+savedCustomer.getCustomerCode());
        clear();
        welcomePage.setVisible(true);
    }

