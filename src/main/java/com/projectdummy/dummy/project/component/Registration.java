    private void CapturePrintsButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        SecugenScanner scanner = SecugenScanner.getInstance();
        byte[] bytesOfImage = scanner.captureBytes();
        customer.addFingerprint(bytesOfImage);

        addPrintToView(scanner.toImage(bytesOfImage));
    }

    private void addPrintToView(BufferedImage image){
        ImageIcon pimage = new ImageIcon(image);

        ImageIcon scaledImage = new ImageIcon(pimage.getImage().getScaledInstance(pimage.getIconWidth()/2,pimage.getIconHeight()/2, Image.SCALE_SMOOTH));

        JLabel labelledImage = new JLabel(scaledImage);



        fingerPanel.add(labelledImage);
        fingerPanel.revalidate();
        fingerPanel.repaint();

    }

